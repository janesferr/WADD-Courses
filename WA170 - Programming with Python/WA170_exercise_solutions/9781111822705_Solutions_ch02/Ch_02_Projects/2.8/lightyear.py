"""
Program: lightyear.py
Project 2.8

Compute the distance that light travels in a year.

Useful facts:
   rate = 3 * 10 ** 8 meters per second
   seconds in a year = 365 * 24 * 60 ** 2

"""         

# Compute the result
rate = 3 * 10 ** 8
seconds = 365 * 24 * 60 ** 2
distance = rate * seconds

# Display the result
print("Light travels", distance, "meters in a year.")
