"""
Program: minutes.py
Project 2.7

Compute the number of minutes in a year.

Useful facts:
   1 year = 365 days (we ignore leap years)
   1 day = 24 hours
   1 hour = 60 minutes

"""         

# Compute the result
minutes = 365 * 24 * 60  

# Display the result
print("The number of minutes in a year is", minutes)
